/*literal is a literal representation of a value in JS.
it is a fixed value, not a variable, they are literally provided in code.
a variable is not a literal, the info assigned to the variable is the literal
ex:
 (keyword)                (value/literal)
     let       placeholder =    'text';
                (variable)
*/

//string literal
let city = 'Indy';

// numeric literal
let num = 123;

// boolean litteral
let isOn = true;

// object literal
let movie = {
    title: ' Elf',
    rating: 'PG',
    release: '03'
};

//array literal
let daysOfWeek = ['Sun', 'Mon', 'Tues', 'Weds', 'Thur', 'Fri', 'Sat', 'Sun'];